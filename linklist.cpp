#include <iostream>
using namespace std;

// Node structure
struct Node {
    int data;
    Node* next;
};

// Insert Operation
void insert(Node*& head, int value) {
    Node* newNode = new Node{value, NULL};

    if (head == NULL) {
        head = newNode;
        return;
    }

    Node* temp = head;
    while (temp->next != NULL)
        temp = temp->next;

    temp->next = newNode;
}

// Delete Operation
void deleteNode(Node*& head, int value) {
    Node* temp = head;
    Node* prev = NULL;

    while (temp != NULL) {
        if (temp->data == value) {
            if (prev == NULL) {
                // Deleting head
                head = head->next;
            } else {
                // Deleting non-head
                prev->next = temp->next;
            }
            delete temp;
            return;
        }
        prev = temp;
        temp = temp->next;
    }

    cout << "Value not found!" << endl;
}

// Traverse Operation
void traverse(Node* head) {
    cout << "Linked List: ";
    while (head != NULL) {
        cout << head->data << " -> ";
        head = head->next;
    }
    cout << "NULL" << endl;
}

int main() {

    Node* head = NULL;

    // Insert
    insert(head, 10);
    insert(head, 20);
    insert(head, 30);

    traverse(head); // Output: 10 -> 20 -> 30 -> NULL

    // Delete
    deleteNode(head, 20);

    traverse(head); // Output: 10 -> 30 -> NULL

    return 0;
}
